#!/usr/bin/env tsx

/**
 * Backfill explicit columns for existing UDB records
 * 
 * This script updates existing records in udb_parsed to populate
 * the explicit columns (udb_*) from raw_data. The trigger will
 * handle this automatically for new records, but this script
 * backfills existing ones.
 * 
 * Usage:
 *   tsx scripts/backfill-udb-explicit-columns.ts
 */

import { config } from 'dotenv';
import { resolve } from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';
import { createAdminClient } from '../app/lib/supabase/server';

// Load environment variables
const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
config({ path: resolve(__dirname, '..', '.env.local') });

/**
 * Backfill explicit columns for all existing records
 */
async function main() {
  console.log('Starting UDB explicit columns backfill...\n');

  // Validate environment variables first
  const supabaseUrl = process.env.SUPABASE_URL;
  const serviceRoleKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

  console.log('Checking Supabase configuration...');
  if (!supabaseUrl) {
    console.error('❌ SUPABASE_URL is not set in .env.local');
    console.error('Please run: npm run setup-supabase');
    process.exit(1);
  }
  if (!serviceRoleKey) {
    console.error('❌ SUPABASE_SERVICE_ROLE_KEY is not set in .env.local');
    console.error('Please run: npm run setup-supabase');
    process.exit(1);
  }

  console.log(`✓ SUPABASE_URL: ${supabaseUrl}`);
  console.log(`✓ SUPABASE_SERVICE_ROLE_KEY: ${serviceRoleKey.substring(0, 20)}... (hidden)`);
  
  // Check if the key looks like a JWT (should have 3 parts separated by dots)
  const keyParts = serviceRoleKey.split('.');
  if (keyParts.length !== 3) {
    console.warn('⚠️  WARNING: SERVICE_ROLE_KEY does not appear to be a valid JWT token');
    console.warn('   It should have 3 parts separated by dots (e.g., xxxxx.yyyyy.zzzzz)');
    console.warn('   For local Supabase, this is usually generated by the setup script.');
    console.warn('   If you\'re using a local Supabase instance, try running: npm run setup-supabase');
  }

  // Initialize Supabase
  let supabase;
  try {
    supabase = createAdminClient();
    console.log('✓ Supabase client initialized\n');
  } catch (error) {
    console.error('❌ Failed to initialize Supabase:', (error as Error).message);
    console.error('Make sure SUPABASE_URL and SUPABASE_SERVICE_ROLE_KEY are set correctly in .env.local');
    process.exit(1);
  }

  try {
    // First, check if the explicit columns exist (migration might not have been run)
    console.log('Checking if explicit columns exist...');
    const { data: testData, error: testError } = await supabase
      .from('udb_parsed')
      .select('udb_year')
      .limit(1);

    if (testError) {
      // Check for column not found error
      if (
        (testError.message?.includes('column') && testError.message?.includes('udb_year')) ||
        testError.code === '42703' // PostgreSQL error code for undefined column
      ) {
        console.error('\n❌ Error: Explicit columns (udb_*) do not exist yet!');
        console.error('Please run the migration first:');
        console.error('  1. Restart Supabase: npm run supabase:restart');
        console.error('  2. Or manually run the SQL in: supabase/db/init/002_add_udb_explicit_columns.sql');
        process.exit(1);
      }
      
      // Check for JWT/auth errors
      if (testError.code === 'PGRST301' || testError.message?.includes('JWT')) {
        console.error('\n❌ JWT Authentication Error:');
        console.error('The SERVICE_ROLE_KEY appears to be invalid or malformed.');
        console.error('\nTroubleshooting:');
        console.error('1. Check that Supabase is running: npm run supabase:status');
        console.error('2. Verify your .env.local has correct values from setup-supabase');
        console.error('3. If using local Supabase, the keys should be generated by setup-supabase');
        console.error('4. Try regenerating: npm run setup-supabase');
        console.error('\nError details:', JSON.stringify(testError, null, 2));
        process.exit(1);
      }
      
      throw testError;
    }

    // Get count of records that need backfilling
    const { count, error: countError } = await supabase
      .from('udb_parsed')
      .select('*', { count: 'exact', head: true });

    if (countError) {
      console.error('Error fetching count:', JSON.stringify(countError, null, 2));
      throw countError;
    }

    console.log(`Found ${count || 0} total records in udb_parsed table`);
    
    if (count === 0) {
      console.log('No records to backfill. Exiting.');
      return;
    }

    console.log('\nUsing SQL function to backfill all records at once (more efficient)...');
    
    // Use a SQL function to backfill all records efficiently
    // This is much faster than updating records one by one
    const { data, error: sqlError } = await supabase.rpc('backfill_udb_explicit_columns');

    if (sqlError) {
      // If the RPC function doesn't exist, fall back to the update method
      if (sqlError.message?.includes('function') || sqlError.code === '42883') {
        console.log('SQL function not found, using update method instead...\n');
        console.log('Note: The database trigger will automatically sync columns from raw_data.');
        console.log('This script triggers the sync by updating raw_data (even if unchanged).\n');

        // Update all records to trigger the sync function
        // We'll do this in batches to avoid overwhelming the database
        const BATCH_SIZE = 1000;
        let offset = 0;
        let totalUpdated = 0;

        while (true) {
          // Fetch a batch of records
          const { data: records, error: fetchError } = await supabase
            .from('udb_parsed')
            .select('id, raw_data')
            .range(offset, offset + BATCH_SIZE - 1);

          if (fetchError) {
            console.error('Error fetching records:', JSON.stringify(fetchError, null, 2));
            throw fetchError;
          }

          if (!records || records.length === 0) {
            break;
          }

          // Update each record to trigger the sync function
          // We update raw_data with itself to trigger the BEFORE UPDATE trigger
          for (const record of records) {
            const { error: updateError } = await supabase
              .from('udb_parsed')
              .update({ raw_data: record.raw_data })
              .eq('id', record.id);

            if (updateError) {
              console.error(`Error updating record ${record.id}:`, updateError.message);
            } else {
              totalUpdated++;
            }
          }

          console.log(`Processed ${totalUpdated} records...`);

          if (records.length < BATCH_SIZE) {
            break;
          }

          offset += BATCH_SIZE;

          // Small delay to avoid overwhelming the database
          await new Promise((resolve) => setTimeout(resolve, 100));
        }

        console.log(`\n✅ Backfill complete! Updated ${totalUpdated} records.`);
        console.log('All explicit columns (udb_*) have been populated from raw_data.');
      } else {
        throw sqlError;
      }
    } else {
      console.log(`\n✅ Backfill complete! Updated records using SQL function.`);
      console.log('All explicit columns (udb_*) have been populated from raw_data.');
    }
  } catch (error) {
    console.error('\n❌ Error during backfill:');
    if (error instanceof Error) {
      console.error('Message:', error.message);
      console.error('Stack:', error.stack);
    } else {
      console.error('Error object:', JSON.stringify(error, null, 2));
      console.error('Error type:', typeof error);
      console.error('Error keys:', Object.keys(error || {}));
    }
    
    // Try to get more details if it's a Supabase error
    if (error && typeof error === 'object' && 'message' in error) {
      console.error('Error details:', error);
    }
    
    process.exit(1);
  }
}

// Run the script
main().catch((error) => {
  console.error('❌ Fatal error:');
  if (error instanceof Error) {
    console.error('Message:', error.message);
    console.error('Stack:', error.stack);
  } else {
    console.error('Error:', JSON.stringify(error, null, 2));
  }
  process.exit(1);
});

